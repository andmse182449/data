	MASACH INT identity(01, 1) NOT NULL PRIMARY KEY,
	TENSACH NVARCHAR(200),
	GIATIEN FLOAT,
	SLTONKHO INT,
	NGAYNHAPHANG DATE,
	NHAXUATBAN NVARCHAR(200),
	MADM int,
