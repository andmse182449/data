create table KHACHHANG(
	MAKH INT identity(01,1) NOT NULL PRIMARY KEY,
	TENKH NVARCHAR(200),
	SDT varchar(220),
	DIACHI NVARCHAR(MAX),
	Email NVARCHAR(MAX),
	Account NVARCHAR(MAX),
	Pass NVARCHAR(MAX),
	MATICHLUY INT
	
	CONSTRAINT FK_MATICHLUY2 FOREIGN KEY (MATICHLUY) REFERENCES TICHLUY (MATICHLUY)
)


create table TACGIA(
	MATG INT identity(01, 1) NOT NULL PRIMARY KEY,
	TENTG NVARCHAR(MAX),
